package com.day5;

import java.util.Scanner;

public class Facto_addition {

	public static void main(String[] args) {
		
		Scanner sc=new Scanner(System.in);
		System.out.println("enter N");
		
		int n=sc.nextInt();
		int S=0;
		int fact;
		
		for(int i=1;i<=n;i++)
		{
			fact=1;
		  for(int j=1;j<=i;j++)	
			  
			  fact*=j;
		  S+=fact;
		}
		}
		
	{

}
{
}
}